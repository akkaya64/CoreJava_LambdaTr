package zoomcalisma.loop;

public class Ex20 {
    public static void main(String[] args) {

        //Aşağıdaki görüntüyü bir for döngüsü kullanarak elde eden kodu yazınız.
        //* * * * * *
        //* * * * *
        //* * * *
        //* * *
        //* *
        //*

        for (int i = 0; i < 6; i++) {
            for (int j = i; j < 6; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }

    }
}
